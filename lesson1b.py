#Variable Naming Guidelines(Rules)
#1. Should always start with a lowercase eg name="Anthony" 
name="Amanda"

# 2. Cannot have space instead use an underscore
first_name="Amanda"
printSuccessMessage="Successfully Done!"

# 3. Cannot start with a digit but can end with a digit
number1=500

# 4. Relevant to the context
secret_key="qwerty123"

