# While Loop
# We use while loop to get infinite loops - (start, stop(limit), step(interval))

# Print 10 numbers

# Start
count = 0

# Stop
while count <= 11:
    print(count)
    # Insert step here
    count = count + 1

print("==========")
# Print numbers from 10 to 4

count = 10
while count >= 4:
    print(count)
    count = count - 1

