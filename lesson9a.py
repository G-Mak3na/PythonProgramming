# Modules/Libraries - a python file containing python functions that can be called from another python file
# The main file is the module

# Create 3 functions
def add (num1, num2):
    sum = num1 + num2
    print(f"The answer is {sum} ")

def multiply (num1, num2):
    product = num1 * num2
    print(f"The answer is {product} ")

def subtract (num1, num2):
    difference = num1 - num2
    print(f"The answer is {difference} ")

def divide (num1, num2):
    division = num1 / num2
    print(f"The answer is {division} ")